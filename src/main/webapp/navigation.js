function navigate(page){

    if(page === "home"){
        window.location.href = "dashboard.html";
    }

    else if(page === "register"){
        window.location.href = "register.html";
    }

    else if(page === "rooms"){
        window.location.href = "room-details.html";
    }

    else if(page === "help"){
        window.location.href = "help.html";
    }

}


